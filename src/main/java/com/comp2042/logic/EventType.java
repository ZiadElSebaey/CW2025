package com.comp2042.logic;

/**
 * Enumeration representing the type of movement event.
 * 
 * @author CW2025 Team
 * @version 1.0
 * @since 1.0
 */
public enum EventType {
    /** Move brick down */
    DOWN, 
    /** Move brick left */
    LEFT, 
    /** Move brick right */
    RIGHT, 
    /** Rotate brick */
    ROTATE
}
